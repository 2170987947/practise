/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> levelOrder(TreeNode* root) {
        vector<vector<int>> ans;
        if (root == nullptr) {
            return ans;
        }
        queue<TreeNode*> queue;
        queue.push(root);
        bool flag = true;
        while (!queue.empty()) {
            int size = queue.size();
            deque<int> deque;
            for (int i = 0; i < size; i++) {
                auto t = queue.front();
                queue.pop();
                if (flag) {
                    deque.push_back(t->val);
                } else {
                    deque.push_front(t->val);
                }
                if (t->left) {
                    queue.push(t->left);
                }
                if (t->right) {
                    queue.push(t->right);
                }
            }
            flag = !flag;
            ans.push_back(vector<int>(deque.begin(), deque.end()));
        }
        return ans;
    }
};
